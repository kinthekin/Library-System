    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String name = txtTitle.getText();
        ArrayList<Book> books = new ArrayList();
        
        try{
            DBEngineLibrary engine = new DBEngineLibrary();
            books = engine.searchBooks(name);
            BookTableModel model = new BookTableModel(books);
            tableBooks.setModel(model);
            jScrollPane1.setViewportView(tableBooks);
        }
        
        catch (Exception exc){
            System.out.println(exc.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

