    private void btnDeleteBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBookActionPerformed
        // TODO add your handling code here:
        int row = tableBooks.getSelectedRow();
          
        //get the current employee to delete
        Book deleteBook = (Book)tableBooks.getValueAt(row, BookTableModel.getOBJECT_COL());
        
         try{
             
             DBEngineLibrary engine = new DBEngineLibrary();
             engine.deleteBook(deleteBook.getID());
             
         }
         catch(Exception exc){
             System.out.println(exc.getMessage());
             
         }

    }//GEN-LAST:event_btnDeleteBookActionPerformed

